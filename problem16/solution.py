# 2^15 = 32768 and the sum of its digits is 3 + 2 + 7 + 6 + 8 = 26.

# What is the sum of the digits of the number 2^1000?
########################################################################################################################
# NOTES
# 
#
########################################################################################################################

from common.util import LOG

def run(args):
    LOG.debug(f"2^1000={2**1000}")
    # Python has a built-in `sum` function, don't even need to import!
    print("SOLUTION", sum([int(c) for c in str(2**1000)]))
